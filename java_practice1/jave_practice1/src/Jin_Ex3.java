
public class Jin_Ex3 {

	public static void main(String[] args) 
	{
		String name = "Ja" + "va"; // 문자열에 "Ja"+"Va"를 넣어라, 
		String str  = name + 8.0; // name변수에 8.0을 더해라(문자열+숫자는 문자열). 

		System.out.println(name);  // "java"
		System.out.println(str); // "JaVa8.0"
		System.out.println(7 + " ");
		System.out.println(" " + 7);
		System.out.println(7 + "");
		System.out.println("" + 7);
		System.out.println("" + "");
		System.out.println(7 + 7 + "");
		System.out.println("" + 7 + 7);
		System.out.println("" + 7 + "jin");
		// 앞에 "" 이후 숫자는 문자열로 취급한다
	
	}

}

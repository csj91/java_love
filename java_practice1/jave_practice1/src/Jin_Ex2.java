
public class Jin_Ex2 {

	public static void main(String[] args) 
	{
		int 	 x = 100 + 1; 
		double pi = 3.14 + 1; 
		char 	 ch = 'a' + 1; 
		String str = "abc" + 1; 
		
		System.out.println(x); // 101
		System.out.println(pi); // 4.140000000000001
		System.out.println(ch); // 'b'
		System.out.println(str); // "abc1" , 문자열+숫자는 문자열로 인식
		

	
	}

}
